    private void attendedKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_attendedKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_DOWN || evt.getKeyCode() == KeyEvent.VK_UP)
        {
            try
        {
           int row = attended.getSelectedRow();
           String table_click = (attended.getModel().getValueAt(row, 0).toString());
           String sql = " SELECT * FROM attended_test WHERE student_id = '"+table_click+"'";
           PreparedStatement pst = con.prepareStatement(sql);
           rs = pst.executeQuery();
           if(rs.next())
           {
               txt_student_id.setText(rs.getString("student_id"));
               txt_first_name.setText(rs.getString("First_Name"));
               txt_surname.setText(rs.getString("surname"));
               txt_name.setText(rs.getString("name"));
               txt_department.setText(rs.getString("department"));
               combo_dp.setSelectedItem(rs.getString("class"));
               image_field.setIcon(ResizeImage(rs.getString("image"),null));
           }
        }catch(Exception e){JOptionPane.showMessageDialog(null,e);}
        }
    }//GEN-LAST:event_attendedKeyPressed

