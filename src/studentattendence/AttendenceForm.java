        public ImageIcon ResizeImage(String image_path, byte[] pic)
        {
         ImageIcon myImage = null;
         if(image_path != null)
         {
             myImage = new ImageIcon(image_path);
         }else
         {
             myImage = new ImageIcon(pic);
         }
         Image img = myImage.getImage();
         Image img2 = img.getScaledInstance(image_field.getWidth(), image_field.getHeight(), Image.SCALE_SMOOTH);
         ImageIcon image = new ImageIcon(img2);
         return image;
        }
